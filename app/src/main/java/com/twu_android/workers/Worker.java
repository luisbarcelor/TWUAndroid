package com.twu_android.workers;

public class Worker {
    public static double sumar(double value1, double value2) {
        return value1 + value2;
    }
    public static double restar(double value1, double value2) {
        return value1 - value2;
    }
    public static double multiplicar(double num1Value, double num2Value) {
        return num1Value * num2Value;
    }

    public static double dividir(double num1Value, double num2Value) {
        return num1Value / num2Value;
    }
}
